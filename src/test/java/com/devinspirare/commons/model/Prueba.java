package com.devinspirare.commons.model;

public class Prueba {
	private String name;
	private String lastName;
	private Integer age;
	private String alfanumerico;
	private String tildacion;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public String getAlfanumerico() {
		return alfanumerico;
	}

	public void setAlfanumerico(String alfanumerico) {
		this.alfanumerico = alfanumerico;
	}

	public String getTildacion() {
		return tildacion;
	}

	public void setTildacion(String tildacion) {
		this.tildacion = tildacion;
	}

}
